#include <stdio.h>
int main()
{
    int i;
    // 1st line
    for (i = 0; i < 8; i++)
    {
        printf(" ");
    }
    printf("*");
    printf("\n");

    // 2nd line
    for (i = 0; i < 11; i++)
    {
        printf("*");
    }
    printf("\n");

    // 3rd line
    for (i = 0; i < 13; i++)
    {
        printf("*");
    }
    printf("\n");

    // 4th line
    for (i = 0; i < 11; i++)
    {
        printf("*");
    }
    printf("\n");

    // 5th line
    for (i = 0; i < 8; i++)
    {
        printf(" ");
    }
    printf("*");
    printf("\n");
}